import React from "react"
import Sdata from "./Sdata"
import Slider from "react-slick"
import "slick-carousel/slick/slick.css"
import "slick-carousel/slick/slick-theme.css"
import { Link } from "react-router-dom"
import { useNavigate } from "react-router-dom"
import { convertURL } from "../redux/Actions/ProductActions"
const SlideCard = () => {
  const settings = {
    dots: true,
    infinite: true,
    slidesToShow: 1,
    slidesToScroll: 1,
    autoplay: true,
    appendDots: (dots) => {
      return <ul style={{ margin: "0px" }}>{dots}</ul>
    },
  }
  const navigate=useNavigate()
  return (
    <>
      <Slider {...settings}>
        {Sdata.map((value, index) => {
          return (
            <>
              <div className='box d_flex top' key={index}>
                <div className='left'>
                  <h1>{value.title}</h1>
                  <p>{value.desc}</p>
                  {value.id===1 ?  <button style={{cursor:"pointer"}} className='btn-primary' onClick={() => navigate(`/pages`)}>Ghé thăm</button> :
                    <button style={{cursor:"pointer"}} className='btn-primary' onClick={() => navigate(`/product/${value.id}`)}>Ghé thăm</button>}
                
                </div>
                <div className='right'>
                  <img src={value.cover} alt='' />
                </div>
              </div>
            </>
          )
        })}
      </Slider>
    </>
  )
}

export default SlideCard
